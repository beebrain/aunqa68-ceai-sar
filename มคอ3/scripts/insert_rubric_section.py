def pick_verb(desc: str, domain: str, level: str) -> str:
    d = desc.strip()
    if "วิเคราะห์" in d and "สังเคราะห์" in d:
        return "วิเคราะห์และสังเคราะห์"
    if "ออกแบบ" in d and "สร้าง" in d:
        return "ออกแบบและสร้าง"
    if "ออกแบบ" in d:
        return "ออกแบบและสร้าง"
    if "จัด" in d.split()[0:2]:
        return "จัด"
    first = d.split()[0] if d else ""
    for key in sorted(RUBRICS.keys(), key=len, reverse=True):
        if first.startswith(key) or key in first or d.startswith(key):
            return key
    dom = domain.lower()
    if dom.startswith("affect"):
        if "รับรู้" in d:
            return "รับรู้"
        if "ตอบสนอง" in d:
            return "ตอบสนอง"
        return "แสดงออก"
    if dom.startswith("psych"):
        if "ปฏิบัติตาม" in d:
            return "ปฏิบัติตาม"
        return "พัฒนา"
    return LEVEL_DEFAULT.get(level.strip(), "ประยุกต์")


def latex_escape(text: str) -> str:
    return text.replace("&", "\\&").replace("%", "\\%")


def build_rubric_block(text: str) -> str:
    clos = CLO_RE.findall(text)
    if not clos:
        return ""
    sub_blocks = []
    for clo_id, desc, domain, level in clos:
        verb = pick_verb(desc, domain, level)
        rows = RUBRICS.get(verb, RUBRICS["ประยุกต์"])
        row_lines = "\n".join(
            f"  \\rubricrow{{{lvl}}}{{{latex_escape(crit)}}}"
            for lvl, crit in rows
        )
        sub_blocks.append(
            f"\\begin{{subrubric}}{{{clo_id}}}{{{latex_escape(desc)}}}\n"
            f"{row_lines}\n"
            f"\\end{{subrubric}}"
        )
    return (
        "\n% ----- หมวดที่ 5 Rubric -----\n"
        "\\begin{rubricsection}\n"
        + "\n\n".join(sub_blocks)
        + "\n\\end{rubricsection}\n"
    )


def update_yearlevel(text: str) -> str:
    for old, new in YEARLEVEL_MAP.items():
        text = text.replace(f"\\yearlevel{{{old}}}", f"\\yearlevel{{{new}}}")
    return text


def process_file(path: Path) -> bool:
    text = path.read_text(encoding="utf-8")
    original = text
    text = update_yearlevel(text)
    block = build_rubric_block(text)
    if block:
        if RUBRIC_BLOCK_RE.search(text):
            text = RUBRIC_BLOCK_RE.sub(lambda _m: block, text)
        else:
            marker = "\\end{weeklyplan}"
            if marker not in text:
                marker = "\\end{assessmenttable}"
            if marker in text:
                text = text.replace(marker, marker + block, 1)
    if text != original:
        path.write_text(text, encoding="utf-8")
        return True
    return False


def main() -> None:
    updated = 0
    for path in sorted(MKO3.glob("AUN3_*.tex")):
        if process_file(path):
            updated += 1
            print(f"updated: {path.name}")
    print(f"done: {updated} files")


if __name__ == "__main__":
    main()
